/**
 * Reads Fashion Biz *biz-care*.csv style titles and writes
 * lib/biz-care-misc-skus.generated.ts (socks / bags / hats → PPE Miscellaneous routing).
 *
 * Run: node scripts/generate-biz-care-misc-skus.mjs
 * Hooked from predev / prebuild via package.json.
 */
import { existsSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";

import { getBossWebRoot } from "./lib/load-env.mjs";

function splitCsvFields(line) {
  const out = [];
  let cur = "";
  let q = false;
  for (let i = 0; i < line.length; i += 1) {
    const c = line[i];
    if (c === '"') {
      q = !q;
      continue;
    }
    if (c === "," && !q) {
      out.push(cur);
      cur = "";
      continue;
    }
    cur += c;
  }
  out.push(cur);
  return out;
}

function isBizCareMiscStyleTitle(title) {
  const t = String(title);
  if (/\b(socks?|tote|bags?|backpack|rucksack|satchel)\b/i.test(t)) {
    return true;
  }
  if (/\b(hat|hats|cap|caps|beanie|beanies|visor|hijab|headwear|head wear|turban|beret)\b/i.test(t)) {
    return true;
  }
  return false;
}

function main() {
  const root = getBossWebRoot();
  const csvDir = join(root, "data", "supplier", "fashion-biz", "csv");
  const codes = new Set();

  if (existsSync(csvDir)) {
    let files = [];
    try {
      files = readdirSync(csvDir).filter(
        (f) => f.toLowerCase().includes("biz-care") && f.toLowerCase().endsWith(".csv"),
      );
    } catch {
      files = [];
    }
    for (const file of files) {
      let text;
      try {
        text = readFileSync(join(csvDir, file), "utf8");
      } catch {
        continue;
      }
      const lines = text.split(/\r?\n/);
      for (let li = 1; li < lines.length; li += 1) {
        const line = lines[li];
        if (!line.trim()) {
          continue;
        }
        const cols = splitCsvFields(line);
        if (cols.length < 3) {
          continue;
        }
        const styleCode = cols[1]?.trim();
        const styleName = cols[2]?.trim();
        if (!styleCode || !styleName) {
          continue;
        }
        if (isBizCareMiscStyleTitle(styleName)) {
          codes.add(styleCode.toUpperCase());
        }
      }
    }
  }

  /** Manual additions → PPE / Miscellaneous (not inferable from CSV title keywords). */
  for (const c of ["C602W", "C603U"]) {
    codes.add(c);
  }

  const sorted = [...codes].sort();
  const outPath = join(root, "lib", "biz-care-misc-skus.generated.ts");
  const body = `/* eslint-disable */
// Auto-generated by scripts/generate-biz-care-misc-skus.mjs — do not edit.

export const BIZ_CARE_MISCELLANEOUS_SKUS_UPPER = new Set<string>([
${sorted.map((c) => `  ${JSON.stringify(c)},`).join("\n")}
]);
`;

  writeFileSync(outPath, body, "utf8");
  console.log(`Wrote ${sorted.length} Biz Care miscellaneous style codes → ${outPath}`);
}

main();
