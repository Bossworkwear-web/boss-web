/**
 * Builds style_code → mens | womens | unisex from Fashion Biz biz-care + biz-collection CSVs.
 *
 * Run: node scripts/generate-fashion-biz-gender.mjs
 */
import { existsSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";

import { getBossWebRoot } from "./lib/load-env.mjs";

function splitCsvFields(line) {
  const out = [];
  let cur = "";
  let q = false;
  for (let i = 0; i < line.length; i += 1) {
    const c = line[i];
    if (c === '"') {
      q = !q;
      continue;
    }
    if (c === "," && !q) {
      out.push(cur);
      cur = "";
      continue;
    }
    cur += c;
  }
  out.push(cur);
  return out;
}

/**
 * @returns {"mens" | "womens" | "unisex"}
 */
function classifyGender(styleName, categoryStr) {
  const title = String(styleName).toLowerCase();
  const tags = String(categoryStr)
    .toLowerCase()
    .split(";")
    .map((t) => t.trim())
    .filter(Boolean);
  const tagSet = new Set(tags);

  if (/\bunisex\b/.test(title) || tagSet.has("unisex")) {
    return "unisex";
  }

  const womenTitle =
    /\bwomens?\b/.test(title) || /\bladies\b/.test(title) || /\blady\b/.test(title);
  const womenTag = tagSet.has("female") || tagSet.has("women");

  const menTitle = /\bmens?\b/.test(title) || /\bmen's\b/.test(title);
  const menTag = tagSet.has("male");

  const hasWomen = womenTitle || womenTag;
  const hasMen = menTitle || menTag;

  if (hasWomen && hasMen) {
    return "unisex";
  }
  if (hasWomen) {
    return "womens";
  }
  if (hasMen) {
    return "mens";
  }
  return "unisex";
}

function mergeTally(a, b) {
  return {
    mens: a.mens + b.mens,
    womens: a.womens + b.womens,
    unisex: a.unisex + b.unisex,
  };
}

function tallyToGender(t) {
  if (t.unisex > 0 && t.mens === 0 && t.womens === 0) {
    return "unisex";
  }
  if (t.mens > 0 && t.womens > 0) {
    return "unisex";
  }
  if (t.womens > 0) {
    return "womens";
  }
  if (t.mens > 0) {
    return "mens";
  }
  return "unisex";
}

function main() {
  const root = getBossWebRoot();
  const csvDir = join(root, "data", "supplier", "fashion-biz", "csv");
  /** @type {Map<string, { mens: number; womens: number; unisex: number }>} */
  const tallies = new Map();

  if (!existsSync(csvDir)) {
    console.warn("No CSV dir:", csvDir);
  } else {
    const files = readdirSync(csvDir).filter(
      (f) =>
        f.toLowerCase().endsWith(".csv") &&
        (f.toLowerCase().includes("biz-care") || f.toLowerCase().includes("biz-collection")),
    );

    for (const file of files) {
      let text;
      try {
        text = readFileSync(join(csvDir, file), "utf8");
      } catch {
        continue;
      }
      const lines = text.split(/\r?\n/);
      for (let li = 1; li < lines.length; li += 1) {
        const line = lines[li];
        if (!line.trim()) {
          continue;
        }
        const cols = splitCsvFields(line);
        if (cols.length < 4) {
          continue;
        }
        const code = cols[1]?.trim();
        const styleName = cols[2]?.trim();
        const category = cols[3]?.trim();
        if (!code || !styleName) {
          continue;
        }
        const g = classifyGender(styleName, category);
        const key = code.toUpperCase();
        const chunk = { mens: 0, womens: 0, unisex: 0 };
        chunk[g] = 1;
        tallies.set(key, mergeTally(tallies.get(key) ?? { mens: 0, womens: 0, unisex: 0 }, chunk));
      }
    }
  }

  /** @type {Record<string, string>} */
  const outRecord = {};
  for (const [code, t] of [...tallies.entries()].sort((a, b) => a[0].localeCompare(b[0]))) {
    outRecord[code] = tallyToGender(t);
  }

  const outPath = join(root, "lib", "fashion-biz-gender.generated.ts");
  const linesOut = Object.keys(outRecord)
    .sort()
    .map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(outRecord[k])},`);

  const body = `/* eslint-disable */
// Auto-generated by scripts/generate-fashion-biz-gender.mjs — do not edit.

export type FashionBizListingGender = "mens" | "womens" | "unisex";

export const FASHION_BIZ_STYLE_GENDER: Record<string, FashionBizListingGender> = {
${linesOut.join("\n")}
};
`;

  writeFileSync(outPath, body, "utf8");
  console.log(`Wrote ${Object.keys(outRecord).length} style codes → ${outPath}`);
}

main();
