/**
 * Biz Care + Biz Collection `*sum*.csv` → style → short_description (column 5).
 * Same shape as Syzmik sum CSV; used when DB `description` has no usable marketing line on category grids.
 *
 * Run: node scripts/generate-biz-care-collection-style-titles.mjs
 */
import { existsSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";

import { getBossWebRoot } from "./lib/load-env.mjs";

function splitCsvFields(line) {
  const out = [];
  let cur = "";
  let q = false;
  for (let i = 0; i < line.length; i += 1) {
    const c = line[i];
    if (c === '"') {
      q = !q;
      continue;
    }
    if (c === "," && !q) {
      out.push(cur);
      cur = "";
      continue;
    }
    cur += c;
  }
  out.push(cur);
  return out;
}

function main() {
  const root = getBossWebRoot();
  const csvDir = join(root, "data", "supplier", "fashion-biz", "csv");
  const map = new Map();
  if (!existsSync(csvDir)) {
    console.warn("Missing CSV dir:", csvDir);
  } else {
    let files;
    try {
      files = readdirSync(csvDir).filter((f) => {
        const l = f.toLowerCase();
        return (
          l.endsWith(".csv") &&
          l.includes("sum") &&
          (l.includes("biz-care") || l.includes("biz-collection"))
        );
      });
    } catch {
      files = [];
    }
    files.sort();
    for (const file of files) {
      let text;
      try {
        text = readFileSync(join(csvDir, file), "utf8");
      } catch {
        continue;
      }
      const lines = text.split(/\r?\n/);
      for (let li = 1; li < lines.length; li += 1) {
        const line = lines[li];
        if (!line.trim()) {
          continue;
        }
        const cols = splitCsvFields(line);
        if (cols.length < 5) {
          continue;
        }
        const style = cols[0]?.trim();
        const shortDesc = cols[4]?.trim();
        if (!style || !shortDesc) {
          continue;
        }
        map.set(style.toUpperCase(), shortDesc);
      }
    }
  }

  const keys = [...map.keys()].sort();
  const linesOut = keys.map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(map.get(k))},`);
  const body = [
    "/* eslint-disable */",
    "// Auto-generated by scripts/generate-biz-care-collection-style-titles.mjs — do not edit.",
    "",
    "/** Biz Care / Biz Collection `*sum*.csv` short_description (grid + PDP when listing name is only `Brand SKU`). */",
    "export const BIZ_CARE_COLLECTION_STYLE_MARKETING_TITLE: Record<string, string> = {",
    ...linesOut,
    "};",
    "",
  ].join("\n");

  const outPath = join(root, "lib", "biz-care-collection-style-titles.generated.ts");
  writeFileSync(outPath, body, "utf8");
  console.log(`Wrote ${map.size} Biz Care/Collection style titles → ${outPath}`);
}

main();
